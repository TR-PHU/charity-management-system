    public void loadListPost() { 
        PreparedStatement ps;
        ResultSet rs;
        String query = "select image,title,id from posts " +
                        "order by created_at desc limit 3";
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            rs = ps.executeQuery();
            rs.next();
            loadImage(rs.getBlob(1), labelImageTop1, titlePostTop1, rs.getString(2));
            postIdTop1 = rs.getInt(3);
            rs.next();
            loadImage(rs.getBlob(1), labelImageTop2, titlePostTop2, rs.getString(2));
            postIdTop2 = rs.getInt(3);
            rs.next();
            loadImage(rs.getBlob(1), labelImageTop3, titlePostTop3, rs.getString(2));
            postIdTop3 = rs.getInt(3);
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void loadImage(Blob imagePost, JLabel label, JLabel title, String text) { 
        ImageIcon myImage = null;
        byte[] imageBytes = null;
        if(imagePost != null) { 
            try {
                imageBytes = imagePost.getBytes(1, (int) imagePost.length());
            } catch (SQLException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            myImage = new ImageIcon(imageBytes);
        } else { 
            File nullImagePost = new File(System.getProperty("user.dir") + "/src/images/image-post-null.jpg");
            String path = nullImagePost.getAbsolutePath();
            myImage = new ImageIcon(path);
        }
        Image image = myImage.getImage();
        Image newImage = image.getScaledInstance(label.getWidth(), label.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(newImage);
        label.setIcon(imageIcon);
        title.setText(text);
    }
    public void loadPostAttention() { 
        PreparedStatement ps;
        ResultSet rs;
        String query = "select post_id, COUNT(post_id) as SL, title,image from user_ref_posts " +
                        "inner join posts " +
                        "on posts.id = user_ref_posts.post_id " +
                        "group by post_id order by sl limit 1";
        try {
            
            
            ps = MyConnection.getConnection().prepareStatement(query);
            rs = ps.executeQuery();
            if(rs.next()) { 
                postIdAttention = rs.getInt(1);
                titleAttention.setText(rs.getString(3));
                ImageIcon myImage = null;
                if(rs.getBlob(4)!= null) { 
                    Blob imagePost = rs.getBlob(4);
                    byte[] imageBytes = imagePost.getBytes(1, (int) imagePost.length());
                    myImage = new ImageIcon(imageBytes);
                } else { 
                    File nullAvatar = new File(System.getProperty("user.dir") + "/src/images/image-post-null.jpg");
                    String path = nullAvatar.getAbsolutePath();
                    myImage = new ImageIcon(path);
                }
                Image image = myImage.getImage();
                Image newImage = image.getScaledInstance(imageAttention.getWidth(), imageAttention.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(newImage);
                imageAttention.setIcon(imageIcon);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
