    private void searchFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchFieldKeyTyped
        String keyword = searchField.getText();
        
        if(!keyword.equals("")) { 
            String query = "select image,title,id from posts " +
                        "where title like '%" + keyword + "%' limit 1";
            Statement st;
            ResultSet rs;


            try {
                st = MyConnection.getConnection().createStatement();

                rs = st.executeQuery(query);

                if(rs.next()) { 
                    loadImage(rs.getBlob(1), imageAttention, titleAttention, rs.getString(2));
                    postIdAttention = rs.getInt(3);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_searchFieldKeyTyped

