    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(filterChooserDate.getDate() != null) { 
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dateDonationMoney = sdf.format(filterChooserDate.getDate());

            DecimalFormat df = new DecimalFormat("#,###.00");
            
            String query = "select sum(donate_money) from user_ref_posts where updated_at >= '" + dateDonationMoney + " 00:00:00'";
            Statement st;
            ResultSet rs;

            try {
                st = MyConnection.getConnection().createStatement();
                rs = st.executeQuery(query);

                if(rs.next()) { 
                    totalMoneyField.setText(df.format(rs.getDouble(1)));
                }
            } catch (SQLException ex) {
                Logger.getLogger(ManageDonateAndPeopleJoinForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

