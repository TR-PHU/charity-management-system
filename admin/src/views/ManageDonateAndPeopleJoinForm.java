    public void loadNumberOfVolunteer() { 
        String query = "select sum(soluotthamgia) from  (select count(user_id) as soluotthamgia, user_id from user_ref_posts " +
                        "where has_evaluated = '0' or has_evaluated = '1' " +
                        "group by user_id) as t";
        
        Statement st;
        ResultSet rs;
        
        try {
            st = MyConnection.getConnection().createStatement();
            rs = st.executeQuery(query);
            
            if(rs.next()) { 
                totalVolunteer.setText(String.valueOf(rs.getInt(1)));
            } 
        } catch (SQLException ex) {
            Logger.getLogger(ManageDonateAndPeopleJoinForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }   
    public  void loadLogo() { 
        File logo = new File(System.getProperty("user.dir") + "/src/images/logo.jpg");
        String path = logo.getAbsolutePath();
        ImageIcon myImage = new ImageIcon(path);
        
        Image image = myImage.getImage();
        Image newImage = image.getScaledInstance(labelLogo.getWidth(), labelLogo.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon imageIcon = new ImageIcon(newImage);
        labelLogo.setIcon(imageIcon);
    }
