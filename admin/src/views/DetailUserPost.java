    public  void loadDataForTableRaiseVolunteer() { 
        DefaultTableModel tableModelRaiseVolunteer = (DefaultTableModel) tableRaiseVolunteer.getModel();
        
        String query = "select title, goal_people from posts " +
                        "where user_id = ? and " +
                        "goal_people > 0";
        
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setInt(1, user.getId());
            rs = ps.executeQuery();
            
            while(rs.next()) { 
                tableModelRaiseVolunteer.addRow(new Object[] { 
                    rs.getString(1),
                    rs.getInt(2)
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(DetailUserPost.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
