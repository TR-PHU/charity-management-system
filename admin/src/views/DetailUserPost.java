    public void loadInfoUser() { 
        String query = "select id, full_name, username, email, gender, is_admin, avatar, status from users where id = ?";
        
        PreparedStatement ps;
        ResultSet rs;
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setInt(1, userId);
            rs = ps.executeQuery();
            ImageIcon myImage = null;
            if (rs.next()) {
                if(rs.getBlob(7) != null) { 
                    Blob avatarUser = rs.getBlob(7);
                    byte[] imageBytes = avatarUser.getBytes(1, (int) avatarUser.length());
                    myImage = new ImageIcon(imageBytes);
                } else { 
                    File nullAvatar = new File(System.getProperty("user.dir") + "/src/images/avatar-null.jpg");
                    String path = nullAvatar.getAbsolutePath();
                    myImage = new ImageIcon(path);
                }
                user = new User(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getInt(5), rs.getInt(6), rs.getBlob(7), rs.getInt(8));
                
                fullNameField.setText(user.getFullName());
                usernameField.setText(user.getUsername());
                emailField.setText(user.getEmail());
                
                if(user.getStatus() == 1) { 
                    statusUser.setText("Hoạt động");
                } else { 
                    statusUser.setText("Bị chặn");
                }
                Image image = myImage.getImage();
                Image newImage = image.getScaledInstance(labelAvatar.getWidth(), labelAvatar.getHeight(), Image.SCALE_SMOOTH);
                ImageIcon imageIcon = new ImageIcon(newImage);
                labelAvatar.setIcon(imageIcon);
            } else { 
                JOptionPane.showMessageDialog(null, "Người dùng không tồn tại", "User not exist", 2);
            } 
        } catch (SQLException ex) {
            Logger.getLogger(DetailUserPost.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
