    private void btnSaveEvaluateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveEvaluateActionPerformed
        String comment = commentField.getText();
        int point = Integer.parseInt(pointField.getText());
        
        if(point < 0 || point > 10) { 
            JOptionPane.showMessageDialog(null, "Giá trị đánh giá không đúng", "Point invalid", 2);
        } else { 
            String queryUpdate = "update user_ref_posts set comment = ?, point = ?,has_evaluated = '1' where user_id = ? and post_id = ?";
            
            PreparedStatement ps;
            
            try {
                ps = MyConnection.getConnection().prepareStatement(queryUpdate);
                ps.setString(1, comment);
                ps.setInt(2, point);
                ps.setInt(3, volunteerId);
                ps.setInt(4, postId);
                
                if(ps.executeUpdate() != 0) { 
                    JOptionPane.showMessageDialog(null, "Đánh giá tình nguyện viên thành công", "Evaluate volunteer success", 2);
                    ManageUserPost manageUserPost = new ManageUserPost(userId);
                    manageUserPost.setVisible(true);
                    manageUserPost.setLocationRelativeTo(null);
                    manageUserPost.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    manageUserPost.pack();
                    this.dispose();
                } else { 
                    JOptionPane.showMessageDialog(null, "Đánh giá tình nguyện viên thất bại", "Evaluate volunteer failed", 2);
                }
            } catch (SQLException ex) {
                Logger.getLogger(EvaluateVolunteerForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSaveEvaluateActionPerformed

