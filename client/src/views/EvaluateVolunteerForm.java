    public void loadFields() { 
        String query = "select full_name, user_id,post_id, has_evaluated, comment, point " +
                        "from user_ref_posts " +
                        "inner join users " +
                        "on user_id = users.id " +
                        "where user_id = ? and post_id = ?";
        
        PreparedStatement ps;
        ResultSet rs;
        
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setInt(1, volunteerId);
            ps.setInt(2, postId);
            
            rs = ps.executeQuery();
            
            if(rs.next()) { 
                evaluateVolunteer = new EvaluateVolunteer(rs.getInt(2), rs.getInt(3), rs.getInt(4), rs.getString(5), rs.getInt(6));
                fullNameVolunteer.setText(rs.getString(1));
                commentField.setText(evaluateVolunteer.getComment());
                pointField.setText(String.valueOf(evaluateVolunteer.getPoint()));
            } else { 
                JOptionPane.showMessageDialog(null, "Unauthorization", "Access deny", 2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(EvaluateVolunteerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
